import java.util.*;

class Main {
    public static void main(String args[]) {
           
           Scanner sc = new Scanner(System.in);
           
           int my_int = sc.nextInt();
           System.out.println(my_int);
           float my_float = sc.nextFloat();
           System.out.println(my_float);
           long my_long = sc.nextLong();
           System.out.println(my_long);
           byte my_byte = sc.nextByte();
           System.out.println(my_byte);
           sc.nextLine();
           String my_char = sc.nextLine();
           System.out.println(my_char);
           
           
         
        
    }
}
